const contractAbi = abiErc1155;

async function validateTokenIdErc1155() {
	
	var isValid = false;
	var contractAddress = document.getElementById("input_9_6").value;
	var adminWallet = document.getElementById("input_9_7").value;
	var tokenIdInput = document.getElementById("input_9_11").value;
	var amountToSell = document.getElementById("input_9_2").value;
	
	//DEBUG
	console.log("contractAddress: " + contractAddress);
	console.log("adminWallet: " + adminWallet);
	console.log("tokenIdInput: " + tokenIdInput);
	console.log("amountToSell: " + amountToSell);
	
	if (tokenIdInput != '') {
		var tokenIds = tokenIdInput;
	} else {
		alert("Please select one token ID");
	}
	
	try {
		// A Web3Provider wraps a standard Web3 provider, which is
		// what MetaMask injects as window.ethereum into each page
		const provider = new ethers.providers.Web3Provider(window.ethereum);

		// The MetaMask plugin also allows signing transactions to
		// send ether and pay to change state within the blockchain.
		// For this, you need the account signer...
		const signer = provider.getSigner();

		const walletAddress = await signer.getAddress();
	
		const contract = new ethers.Contract(contractAddress, contractAbi, provider);
		
		// For ERC1155, a token ID could have more than one amount. 
		let tokenBalance = await contract.balanceOf(walletAddress, tokenIds);
		
		//DEBUG
		console.log("tokenBalance: " + tokenBalance);
		
		if ( parseInt(tokenBalance) >= parseInt(amountToSell) ) {
			isValid = true;
			
		} else {
			isValid = false;
			alert("You do not have sufficient token to sell. Please change the quantity.");
		}

	} catch (error) {
		console.log(error);
	}
	
	return isValid;
}

